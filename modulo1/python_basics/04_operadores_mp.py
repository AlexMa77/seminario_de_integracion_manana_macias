# Operadores Matematicos
nota_1 = 15
nota_2 = 20
print("Suma")
print(nota_1, "+", nota_2, "=", nota_1 + nota_2)
print("Resta")
print(nota_1, "-", nota_2, "=", nota_1 - nota_2)
print("Multiplicacion")
print(nota_1, "*", nota_2, "=", nota_1 * nota_2)
print("Potencia")
print(nota_1, "**", nota_2, "=", nota_1 ** nota_2)
print("Division")
print(nota_1, "/", nota_2, "=", nota_1 / nota_2)
print("Division Entera")
print(nota_1, "//", nota_2, "=", nota_1 // nota_2)
print("Modulo")
print(nota_1, "%", nota_2, "=", nota_1 % nota_2)
print("Operadores de Asignacion")
print("========================")
cupos = 10
cupos += 5
print("cupos+=5", cupos)
cupos -= 3
print("cupos-=3", cupos)
cupos *= 2
print("cupos*=2", cupos)
cupos //= 2
print("cupos//=2", cupos)
cupos **= 2
print("cupos**=2", cupos)
